# Escape Sequence
# \n - new line

a = "batman is a super hero and \n he is very \"strong\"" 
print(a)

# \t - tab space

a = "batman is a super hero and \t he is very  strong"
print(a)

# \\ - backslash
a = "batman is a super hero \\ and he is very strong"
print(a)

# \' - single quote
a = 'batman is a super hero and he is very strong \'and he is very strong\''
print(a)